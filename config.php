<?php
// ========================================
// config.php - ГЛАВНЫЕ НАСТРОЙКИ ПРИЛОЖЕНИЯ
// ========================================
// Это важный файл! Здесь хранятся все настройки для работы сайта

// ВКЛЮЧАЕМ РЕЖИМ ДЛЯ РАЗРАБОТКИ
// Это нужно, чтобы видеть ошибки при разработке (потом нужно отключить!)
error_reporting(E_ALL); // Показываем ВСЕ ошибки
ini_set('display_errors', 1); // Выводим ошибки на экран

// ========================================
// НАСТРОЙКИ БАЗЫ ДАННЫХ
// ========================================
// define() создаёт переменную, которая не может быть изменена
// Эти настройки говорят PHP, как подключиться к MySQL базе данных

define('DB_HOST', 'localhost');           // Адрес компьютера где находится БД (обычно localhost)
define('DB_NAME', 'volga_expanses');      // Название базы данных
define('DB_USER', 'root');                // Имя пользователя БД (по умолчанию root)
define('DB_PASS', '');                    // Пароль БД (обычно пустой на локальном компьютере)

// ========================================
// СЕССИЯ
// ========================================
// Сессия - это способ сохранить информацию о пользователе когда он на сайте
// Например, мы можем сохранить, что пользователь вошёл в систему
session_start(); // Начинаем сессию

// ========================================
// ФУНКЦИЯ: Подключение к базе данных
// ========================================
// Эта функция создаёт подключение к MySQL БД
// Её вызывают из класса Database
function getDBConnection() {
    try {
        // try - пробуем выполнить код
        // new PDO() - создаёт новое подключение к БД
        // PDO - это способ работы с базами данных в PHP
        
        $pdo = new PDO(
            // Адрес БД в формате: mysql:host=адрес;dbname=название;charset=кодировка
            "mysql:host=" . DB_HOST . ";dbname=" . DB_NAME . ";charset=utf8",
            DB_USER, // Используем имя пользователя из define выше
            DB_PASS  // Используем пароль из define выше
        );
        
        // setAttribute() - устанавливает поведение БД
        // Мы говорим: если будет ошибка, выбросить исключение (Exception)
        // Исключение - это способ сообщить об ошибке
        $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
        
        // Если всё прошло успешно, возвращаем подключение $pdo
        return $pdo;
        
    } catch(PDOException $e) {
        // catch - ловим ошибку если она произошла
        // PDOException - это специальная ошибка от БД
        
        // die() - останавливает выполнение скрипта
        // Выводим сообщение об ошибке
        die("❌ КРИТИЧЕСКАЯ ОШИБКА: Не удалось подключиться к базе данных!\n" . 
            "Сообщение об ошибке: " . $e->getMessage());
    }
}

// ========================================
// КОНЕЦ config.php
// ========================================

?>
